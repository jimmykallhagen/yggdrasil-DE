# =============================================
# DEFAULT SETTINGS
# =============================================
DEFAULT_SETTINGS = {
    'pass_mouse_when_bound': False,
    'workspace_back_and_forth': False,
    'hide_special_on_workspace_change': True,
    'allow_workspace_cycles': True,
    'workspace_center_on': 1,
    'focus_preferred_method': 0,
    'ignore_group_lock': False,
    'movefocus_cycles_fullscreen': False,
    'movefocus_cycles_groupfirst': False,
    'disable_keybind_grabbing': False,
    'window_direction_monitor_fallback': False,
    'allow_pin_fullscreen': False,
    'drag_threshold': 0,
}


# =============================================
# FILE PATH
# =============================================
CONFIG_PATH = Path.home() / ".config" / "nordix" / "yggdrasil" / "settings" / "Yggdrasil-binds.conf"


# =============================================
# SETTINGS HANDLER
# =============================================
class BindsSettings:
    """Handles reading and writing of nordix-binds.conf"""

    def __init__(self):
        self.settings = DEFAULT_SETTINGS.copy()
        self.load_config()

    def load_config(self):
        """Read current settings from config file"""
        if not CONFIG_PATH.exists():
            print(f"Config not found: {CONFIG_PATH}, using defaults")
            return

        try:
            content = CONFIG_PATH.read_text()

            in_binds = False
            brace_depth = 0

            for line in content.split('\n'):
                stripped = line.strip()

                if stripped.startswith('# ') or not stripped:
                    continue

                if re.match(r'^binds\s*\{', stripped):
                    in_binds = True
                    brace_depth = 1
                    continue

                if '{' in stripped:
                    brace_depth += stripped.count('{')
                if '}' in stripped:
                    brace_depth -= stripped.count('}')
                    if brace_depth <= 0:
                        in_binds = False
                        brace_depth = 0
                    continue

                if not in_binds:
                    continue

                if '=' in stripped:
                    parts = stripped.split('=', 1)
                    if len(parts) == 2:
                        key = parts[0].strip()
                        value = parts[1].strip()

                        if '# ' in value:
                            value = value.split('# ')[0].strip()

                        if key in self.settings:
                            current_type = type(self.settings[key])
                            try:
                                if current_type == bool:
                                    self.settings[key] = value.lower() == 'true'
                                elif current_type == int:
                                    self.settings[key] = int(value)
                                elif current_type == float:
                                    self.settings[key] = float(value)
                                else:
                                    self.settings[key] = value
                            except ValueError:
                                pass

        except Exception as e:
            print(f"Error loading config: {e}")

    def generate_config(self):
        """Generate config file content"""
        s = self.settings

        def b(val):
            return 'true' if val else 'false'

        config = f'''
##=============================================##
 #        Yggdrasil's -  Binds Settings        #
 #          Hyprland - Configuration           #
##=============================================##
 # * Generated by Nordix Binds GUI             #
##=============================================###
binds {{

##======================##
 #     MOUSE & DRAG     #
##======================##

# Don't pass mouse events to apps when a keybind is triggered (bool)
pass_mouse_when_bound = {b(s['pass_mouse_when_bound'])}

# Movement threshold in pixels for window dragging, 0 = grab on mousedown (int)
drag_threshold = {s['drag_threshold']}


##======================##
 #  WORKSPACE BEHAVIOR  #
##======================##

# Switch to previous workspace when pressing current workspace key (bool)
workspace_back_and_forth = {b(s['workspace_back_and_forth'])}

# Hide special workspace when changing active workspace (bool)
hide_special_on_workspace_change = {b(s['hide_special_on_workspace_change'])}

# Allow workspace cycles by remembering previous workspace (bool)
allow_workspace_cycles = {b(s['allow_workspace_cycles'])}

# Center cursor on workspace (0) or last active window (1) when switching (int)
workspace_center_on = {s['workspace_center_on']}


##======================##
 #    FOCUS BEHAVIOR    #
##======================##

# Focus method: 0 = history (recent priority), 1 = length (longer shared edges) (int)
focus_preferred_method = {s['focus_preferred_method']}

# Cycle fullscreen when using movefocus on a fullscreen window (bool)
movefocus_cycles_fullscreen = {b(s['movefocus_cycles_fullscreen'])}

# Cycle group windows first before moving to other windows (bool)
movefocus_cycles_groupfirst = {b(s['movefocus_cycles_groupfirst'])}

# Move window/focus to next monitor when reaching monitor edge (bool)
window_direction_monitor_fallback = {b(s['window_direction_monitor_fallback'])}


##======================##
 #     GROUP & LOCK     #
##======================##

# Ignore group lock for move dispatchers (bool)
ignore_group_lock = {b(s['ignore_group_lock'])}

# Allow fullscreen on pinned windows (bool)
allow_pin_fullscreen = {b(s['allow_pin_fullscreen'])}


##======================##
 #   KEYBIND CONTROL    #
##======================##

# Prevent apps (e.g. VMs) from disabling keybinds (bool)
disable_keybind_grabbing = {b(s['disable_keybind_grabbing'])}


}}
'''
        return config

    def save_config(self):
        """Save configuration to file"""
        try:
            CONFIG_PATH.parent.mkdir(parents=True, exist_ok=True)

            if CONFIG_PATH.exists():
                backup = CONFIG_PATH.with_suffix('.conf.backup')
                shutil.copy2(CONFIG_PATH, backup)

            CONFIG_PATH.write_text(self.generate_config())
            return True
        except Exception as e:
            print(f"Error saving config: {e}")
            return False

    def reload_hyprland(self):
        """Reload Hyprland configuration"""
        try:
            subprocess.run(['hyprctl', 'reload'], check=True)
            return True
        except Exception as e:
            print(f"Error reloading Hyprland: {e}")
            return False

    def reset_to_defaults(self):
        """Reset all settings to defaults"""
        self.settings = DEFAULT_SETTINGS.copy()


