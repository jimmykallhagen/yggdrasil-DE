class NordixBindsWindow(Adw.ApplicationWindow):
    """Main window for binds settings"""

    def __init__(self, app):
        super().__init__(application=app, title="Yggdrasil Binds Settings")
        self.set_default_size(550, 700)

        self.settings = BindsSettings()
        self.widgets = {}

        self.setup_ui()

    def setup_ui(self):
        # Main container
        main_box = Gtk.Box(orientation=Gtk.Orientation.VERTICAL)

        # Header bar
        header = Adw.HeaderBar()

        apply_btn = Gtk.Button(label="Apply")
        apply_btn.add_css_class("suggested-action")
        apply_btn.connect("clicked", self.on_apply)
        header.pack_end(apply_btn)

        reset_btn = Gtk.Button(label="Reset to Default")
        reset_btn.connect("clicked", self.on_reset)
        header.pack_start(reset_btn)

        main_box.append(header)

        # Scrollable content
        scrolled = Gtk.ScrolledWindow(vexpand=True)
        scrolled.set_policy(Gtk.PolicyType.NEVER, Gtk.PolicyType.AUTOMATIC)

        content = Gtk.Box(orientation=Gtk.Orientation.VERTICAL, spacing=12)
        content.set_margin_top(12)
        content.set_margin_bottom(12)
        content.set_margin_start(12)
        content.set_margin_end(12)

        # =========================================
        # MOUSE & DRAG SECTION
        # =========================================
        mouse_group = Adw.PreferencesGroup(
            title="Mouse and Drag",
            description="Mouse behavior when keybinds are active"
        )

        self.widgets['pass_mouse_when_bound'] = SwitchRow(
            "Pass Mouse When Bound",
            "Pass mouse events to apps even when a keybind is triggered",
            self.settings.settings['pass_mouse_when_bound'],
            lambda v: self.on_value_changed('pass_mouse_when_bound', v)
        )
        mouse_group.add(self.widgets['pass_mouse_when_bound'])

        self.widgets['drag_threshold'] = SliderRow(
            "Drag Threshold",
            "Pixel movement before drag starts, 0 = instant grab [0 - 20]",
            0, 20, 1, self.settings.settings['drag_threshold'],
            lambda v: self.on_value_changed('drag_threshold', int(v))
        )
        mouse_group.add(self.widgets['drag_threshold'])

        content.append(mouse_group)

        # =========================================
        # WORKSPACE BEHAVIOR SECTION
        # =========================================
        workspace_group = Adw.PreferencesGroup(
            title="Workspace Behavior",
            description="How workspace switching behaves"
        )

        self.widgets['workspace_back_and_forth'] = SwitchRow(
            "Back and Forth",
            "Pressing current workspace key switches to previous workspace",
            self.settings.settings['workspace_back_and_forth'],
            lambda v: self.on_value_changed('workspace_back_and_forth', v)
        )
        workspace_group.add(self.widgets['workspace_back_and_forth'])

        self.widgets['hide_special_on_workspace_change'] = SwitchRow(
            "Hide Special on Change",
            "Hide special workspace when switching workspaces",
            self.settings.settings['hide_special_on_workspace_change'],
            lambda v: self.on_value_changed('hide_special_on_workspace_change', v)
        )
        workspace_group.add(self.widgets['hide_special_on_workspace_change'])

        self.widgets['allow_workspace_cycles'] = SwitchRow(
            "Allow Workspace Cycles",
            "Workspaces remember previous workspace for cycling",
            self.settings.settings['allow_workspace_cycles'],
            lambda v: self.on_value_changed('allow_workspace_cycles', v)
        )
        workspace_group.add(self.widgets['allow_workspace_cycles'])

        self.widgets['workspace_center_on'] = DropdownRow(
            "Center Cursor On",
            "Where to center cursor when switching workspaces",
            [
                {'value': 0, 'name': 'Workspace center'},
                {'value': 1, 'name': 'Last active window'},
            ],
            self.settings.settings['workspace_center_on'],
            lambda v: self.on_value_changed('workspace_center_on', v)
        )
        workspace_group.add(self.widgets['workspace_center_on'])

        content.append(workspace_group)

        # =========================================
        # FOCUS BEHAVIOR SECTION
        # =========================================
        focus_group = Adw.PreferencesGroup(
            title="Focus Behavior",
            description="How window focus is determined"
        )

        self.widgets['focus_preferred_method'] = DropdownRow(
            "Focus Method",
            "How focus target is chosen when moving in a direction",
            [
                {'value': 0, 'name': 'History (recent windows first)'},
                {'value': 1, 'name': 'Length (longer shared edges first)'},
            ],
            self.settings.settings['focus_preferred_method'],
            lambda v: self.on_value_changed('focus_preferred_method', v)
        )
        focus_group.add(self.widgets['focus_preferred_method'])

        self.widgets['movefocus_cycles_fullscreen'] = SwitchRow(
            "Cycle Fullscreen",
            "Movefocus cycles fullscreen state instead of direction",
            self.settings.settings['movefocus_cycles_fullscreen'],
            lambda v: self.on_value_changed('movefocus_cycles_fullscreen', v)
        )
        focus_group.add(self.widgets['movefocus_cycles_fullscreen'])

        self.widgets['movefocus_cycles_groupfirst'] = SwitchRow(
            "Cycle Group First",
            "Cycle through group windows before moving to other windows",
            self.settings.settings['movefocus_cycles_groupfirst'],
            lambda v: self.on_value_changed('movefocus_cycles_groupfirst', v)
        )
        focus_group.add(self.widgets['movefocus_cycles_groupfirst'])

        self.widgets['window_direction_monitor_fallback'] = SwitchRow(
            "Monitor Fallback",
            "Move focus/window to next monitor when reaching edge",
            self.settings.settings['window_direction_monitor_fallback'],
            lambda v: self.on_value_changed('window_direction_monitor_fallback', v)
        )
        focus_group.add(self.widgets['window_direction_monitor_fallback'])

        content.append(focus_group)

        # =========================================
        # GROUP & LOCK SECTION
        # =========================================
        group_section = Adw.PreferencesGroup(
            title="Group and Lock",
            description="Group lock and pinning behavior"
        )

        self.widgets['ignore_group_lock'] = SwitchRow(
            "Ignore Group Lock",
            "Move dispatchers ignore group lock state",
            self.settings.settings['ignore_group_lock'],
            lambda v: self.on_value_changed('ignore_group_lock', v)
        )
        group_section.add(self.widgets['ignore_group_lock'])

        self.widgets['allow_pin_fullscreen'] = SwitchRow(
            "Allow Pin Fullscreen",
            "Allow fullscreen on pinned windows and restore pin after",
            self.settings.settings['allow_pin_fullscreen'],
            lambda v: self.on_value_changed('allow_pin_fullscreen', v)
        )
        group_section.add(self.widgets['allow_pin_fullscreen'])

        content.append(group_section)

        # =========================================
        # KEYBIND CONTROL SECTION
        # =========================================
        keybind_group = Adw.PreferencesGroup(
            title="Keybind Control",
            description="How apps interact with keybinds"
        )

        self.widgets['disable_keybind_grabbing'] = SwitchRow(
            "Disable Keybind Grabbing",
            "Prevent apps (e.g. VMs) from requesting keybinds to be disabled",
            self.settings.settings['disable_keybind_grabbing'],
            lambda v: self.on_value_changed('disable_keybind_grabbing', v)
        )
        keybind_group.add(self.widgets['disable_keybind_grabbing'])

        content.append(keybind_group)

        scrolled.set_child(content)
        main_box.append(scrolled)

        # Status bar
        self.status_bar = Gtk.Label(label="Ready")
        self.status_bar.add_css_class("dim-label")
        self.status_bar.set_margin_top(6)
        self.status_bar.set_margin_bottom(6)
        main_box.append(self.status_bar)

        self.set_content(main_box)

    def on_value_changed(self, key, value):
        """Update a setting"""
        self.settings.settings[key] = value
        self.status_bar.set_text(f"Changed: {key} = {value}")

    def on_apply(self, button):
        """Save and apply changes"""
        if self.settings.save_config():
            if self.settings.reload_hyprland():
                self.status_bar.set_text("Saved and applied successfully!")
            else:
                self.status_bar.set_text("Saved (could not reload Hyprland)")
        else:
            self.status_bar.set_text("Error saving configuration!")

    def on_reset(self, button):
        """Reset to default values"""
        self.settings.reset_to_defaults()
        self.refresh_ui()
        self.status_bar.set_text("Reset to Nordix defaults — click Apply to save")

    def refresh_ui(self):
        """Update all widgets with current values"""
        s = self.settings.settings

        for key, widget in self.widgets.items():
            if key in s:
                if isinstance(widget, SliderRow):
                    widget.set_value(s[key])
                elif isinstance(widget, SwitchRow):
                    widget.set_active(s[key])
                elif isinstance(widget, DropdownRow):
                    widget.set_value(s[key])


