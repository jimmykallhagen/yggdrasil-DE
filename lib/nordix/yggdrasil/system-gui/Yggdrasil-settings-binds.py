class NordixBindsApp(Adw.Application):
    """Main application"""

    def __init__(self):
        super().__init__(
            application_id='com.Nordix.Settings.Binds',
            flags=Gio.ApplicationFlags.FLAGS_NONE
        )

    def do_activate(self):
        win = NordixBindsWindow(self)
        win.present()


def main():
    app = NordixBindsApp()
    return app.run(None)


